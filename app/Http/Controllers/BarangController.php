     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request,Barang $barang)
    {
        $request->validate([
            'nama_barang' => ['required'],
            'merk' => ['required'],
            'tipe' => ['required'],
            'jumlah' => ['required'],
            // 'gambar' => ['nullable'],
        ]);

        $store = $request->all();
        $barang->create($store);

        return redirect()->route('barang.index');
    }

